public class Loop {
  public static void main(String[] args) {
    int num = 1; // initialization
    while (num <= 10) { // condition
      System.out.println(num); // actual work
      num++; // updation
    }

    int count = 500;
    while (count >= 200) {
      System.out.println(count);
      count--;
    }
  }
}
